/**
 * 构建阿卡西之影的完整 System Prompt。
 * Char 模式：注入角色上下文 + 天象 + 对话指令
 * User 模式：仅注入天象，纯自由对话
 */
export function buildAkashicShadowsPrompt(params: {
  isUser: boolean;
  userName: string;
  ephemerisData?: string;
  charProfile?: CharacterProfile;
}): string {
  const { isUser, userName, ephemerisData, charProfile } = params;

  // ── 1. 人格基底 ──
  let prompt = OLD_GOD_PERSONA + '\n\n';

  if (!isUser && charProfile) {
    // ── Char 模式 ──
    const charName = charProfile.name || '命运交织之人';

    // 2. 寻路人档案
    prompt += `## 寻路人\n- 名字：${userName}\n\n`;

    // 3. 被窥探之人
    prompt += `## 被窥探的灵魂：${charName}\n`;
    prompt += extractCharContext(charProfile, userName, '尤拉诺斯') + '\n';

    // 4. 今日天象
    if (ephemerisData) {
      prompt += `## 今日天象\n${ephemerisData}\n\n`;
    }

    // 5. 对话指令
    prompt += AKASHIC_SHADOWS_DIRECTIVE + '\n\n';
  } else {
    // ── User 模式 ──
    // 仅天象，不注入任何人设
    if (ephemerisData) {
      prompt += `## 今日天象\n${ephemerisData}\n\n`;
    }
    prompt += `\n每次回复不超过250字。以散文体输出，以中文回复。\n\n`;
  }

  // ── 文风 + 禁忌 ──
  prompt += OLD_GOD_STYLE_DIRECTIVES;

  return prompt;
}
