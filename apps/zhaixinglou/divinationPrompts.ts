/**
 * 从 CharacterProfile 中安全提取精简上下文（用于解盘 prompt 注入）。
 * 严禁使用 ContextBuilder.buildCoreContext()，那会注入流水账日志导致 token 爆炸。
 */
export function extractCharContext(char: CharacterProfile, userName: string, personaName: string = '命运之神'): string {
  let ctx = '';

  // 1. 核心人设（必须）
  ctx += `### ${char.name}的核心设定\n`;
  ctx += `（以下是关于${char.name}这个人的基本信息，供你了解其本质。）\n`;
  ctx += `${char.systemPrompt || '(无)'}\n\n`;

  // 2. 世界观（可选）
  if (char.worldview?.trim()) {
    ctx += `### ${char.name}所处的世界\n${char.worldview}\n\n`;
  }

  // 3. 印象（可选，精简提取关键字段）
  if (char.impression) {
    const imp = char.impression;
    ctx += `### ${char.name}对${userName}的内心看法\n`;
    ctx += `（⚠ 这是${char.name}的私密档案，记录了TA对${userName}的真实感受。\n`;
    ctx += `你作为${personaName}洞悉这一切，但不应直白转述，而是化为星象或牌面的隐喻。）\n`;
    ctx += `- 核心评价: ${imp.personality_core?.summary || '未知'}\n`;
    ctx += `- 互动模式: ${imp.personality_core?.interaction_style || '未知'}\n`;
    ctx += `- 观察到的特质: ${imp.personality_core?.observed_traits?.join(', ') || '未知'}\n`;
    ctx += `- TA的喜好: ${imp.value_map?.likes?.join(', ') || '未知'}\n`;
    ctx += `- 情绪雷区: ${imp.emotion_schema?.triggers?.negative?.join(', ') || '未知'}\n`;
    ctx += `- 舒适区: ${imp.emotion_schema?.comfort_zone || '未知'}\n`;
    if (imp.observed_changes && imp.observed_changes.length > 0) {
      ctx += `- 最近变化: ${imp.observed_changes.join('; ')}\n`;
    }
    ctx += '\n';
  }

  // 4. 核心记忆（可选，仅 refinedMemories，严禁流水账 memories）
  if (char.refinedMemories && Object.keys(char.refinedMemories).length > 0) {
    ctx += `### ${char.name}的记忆档案\n`;
    ctx += `（⚠ 注意：以下记忆以${char.name}的第一人称视角书写，这是TA的内心独白，\n`;
    ctx += `不是你${personaName}的视角。你作为第三方观察者审视这些记忆。）\n`;
    Object.entries(char.refinedMemories).sort().forEach(([date, summary]) => {
      ctx += `- [${date}]: ${summary}\n`;
    });
    ctx += '\n';
  }

  return ctx;
}

